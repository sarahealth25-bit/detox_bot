async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    uid = update.effective_user.id
    USERS[uid] = {"step": 0, "yes": 0}
    kb = InlineKeyboardMarkup([[InlineKeyboardButton("🚀 Начать тест", callback_data="start")]])
    await update.message.reply_photo(photo=WELCOME_PHOTO, caption=WELCOME_TEXT, reply_markup=kb)

async def button(update: Update, context: ContextTypes.DEFAULT_TYPE):
    q = update.callback_query
    await q.answer()
    uid = q.from_user.id

    if q.data in ("start", "restart"):
        USERS[uid] = {"step": 0, "yes": 0}
        kb = InlineKeyboardMarkup([[
            InlineKeyboardButton("✅ Да", callback_data="да_0"),
            InlineKeyboardButton("❌ Нет", callback_data="нет_0"),
        ]])
        await q.message.reply_text(QUESTIONS[0], reply_markup=kb)
        return

    if "_" not in q.data:
        return

    parts = q.data.rsplit("_", 1)
    if len(parts) != 2:
        return

    answer_val = parts[0]
    try:
        q_index = int(parts[1])
    except ValueError:
        return

    if answer_val not in ("да", "нет"):
        return

    if uid not in USERS:
        USERS[uid] = {"step": 0, "yes": 0}

    current_step = USERS[uid]["step"]

    if q_index != current_step:
        return

    answer = "✅ Да" if answer_val == "да" else "❌ Нет"
    if answer_val == "да":
        USERS[uid]["yes"] += 1

    await q.edit_message_text(QUESTIONS[current_step] + f"\n\nТвой ответ: {answer}")

    next_step = current_step + 1
    USERS[uid]["step"] = next_step

    if next_step < len(QUESTIONS):
        kb = InlineKeyboardMarkup([[
            InlineKeyboardButton("✅ Да", callback_data=f"да_{next_step}"),
            InlineKeyboardButton("❌ Нет", callback_data=f"нет_{next_step}"),
        ]])
        await q.message.reply_text(QUESTIONS[next_step], reply_markup=kb)
    else:
        yes = USERS[uid]["yes"]
        USERS[uid] = {"step": 0, "yes": 0}
        chat_id = q.message.chat_id

        await context.bot.send_photo(
            chat_id=chat_id,
            photo=get_result_photo(yes),
            caption=get_result_text(yes)
        )

        context.job_queue.run_once(send_msg2, when=42, data={"chat_id": chat_id, "yes": yes})
        context.job_queue.run_once(send_msg3, when=102, data={"chat_id": chat_id, "yes": yes})

app = ApplicationBuilder().token(TOKEN).build()
